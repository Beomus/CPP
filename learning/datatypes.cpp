#include <iostream>

using namespace std;

int main() {
    // char: character datatype, using single quote for a single character
    char var1 = 'A';

    // string: sequence of char, using a double quote
    string var2 = "A bunch of different characters.";

    // int: integer
    int var3 = 10;

    // float: float
    float var4 = 1.5;

    // double: more decimal points than float
    double var5 = 1.230;

    // boolean: well boolean, all lowercase
    bool var6 = true;

    return 0;
}